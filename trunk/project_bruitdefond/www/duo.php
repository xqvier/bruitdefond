

<article><img src="./images/photos page 3 - Sylvy.jpg"/> Sylvy a chanté pour la première fois sur scène à l'âge de 11 ans. Ce fût le début d'une aventure musicale où se sont
enchainée diverses prestations scéniques (gala, téléthon, spectacle des restos du coeur, fête votive, ...)
Par la suite, elle fût chanteuse dans l'orchestre « Vibration » pendant 3 années, et continua sa route en tant que choriste 
 le JH club Millavois. </article>
 
<article><img src="./images/photos  page 3 - Pat.jpg"/>  Pat a débuté sa carrière en tant que choriste dans l'orchestre « Larsen » (2000/2002), puis il s'est mis à aux 
 percussions. Eté 2002, il rentre dans la fanfare de la compagnie du petit vélo à   la caisse claire. Début 2003, avec quatre autres musiciens, ils fonda 
 « Les Allumés du Pouce » (ska punk). Il y resta batteur pendant quatre saisons et devenu par la suite leur manager (et il l'est 
 Toujours ! ). dès qu'il en a eu l'occasion, Pat repris le chant... !! 
  Sylvy et Pat se rencontrèrent octobre 2012, et décidèrent de créer « Bruit de Fond »